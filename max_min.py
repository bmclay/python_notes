# max() 
# evaluates the argument and returns the max value. 
# when used on strings it passes the character with the highest index value
big = max('Hello World')
print(big)

highest = max(1, 3, 20, 1005)
print(highest)

#min()
# evaluates the argument and returns the min value.
# when used on strings it passes the character with the lowest index value
small = min('stringz')
print(small)

lowest = min( 1, 10, 2, 100, 0)
print(lowest)


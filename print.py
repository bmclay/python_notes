# print a string on the screen
# Must use ' ' to pass strings on to python. Passes the literal characters that are enclosed.
print('Hello World')
# print other data types on the screen
# Python can enterperate other data types without '
print(4.6)
# You can print multiple things using one statement
print(4.6, "Hello World", False)
# You can end the print statement by specifying end in the statement. 
# Tell python to end, and bring the cursor to a new line:
print ("Hello World", end='\n')
# Unless specified otherwise, print adds the \n to bring to a new line.
# end, but with a character and no new line:
print('Hello World', end='|')
print('still on the same line')
